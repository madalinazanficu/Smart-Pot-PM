void drop(const uint8_t vec[], const int size) {
  FastLED.clear();
  
  for (int i = 0; i < 255; i++) {
    leds[i] = CRGB::Black;
  }

  for (int i = 0; i < size; i++) {
    uint8_t read = pgm_read_byte_near(vec + i);
    leds[read] = CRGB::Blue;
  }

  FastLED.show();
}

